/**
 * 작업사례 상세 구성 (마이너레이저·출력하라 벤치마킹).
 * "제작했습니다"로 끝내지 않고 요청 → 소재·가공 → 주의점 → 결과 → 주문 안내
 * 순서로 서술해 게시물 하나가 검색 유입과 견적 전환을 담당한다.
 */
export interface CaseStudy {
  /** 제작 요청 내용 — 어떤 고객이 어떤 상황에서 주문했는지. */
  request: string;
  /** 사용한 소재 설명. */
  materialDetail: string;
  /** 가공방식 설명 — 어떻게 만들었는지. */
  methodDetail: string;
  /** 제작 시 주의점 — 견적·품질에 영향을 주는 조건. */
  cautions: string[];
  /** 완성 결과. */
  result: string;
  /** 같은 제작을 주문할 때 필요한 정보 체크리스트. */
  orderInfo: string[];
}

export interface PortfolioItem {
  slug: string;
  category: CategorySlug;
  title: string;
  description: string;
  /** 대표 소재. */
  material: string;
  /** 제작 방식. */
  method: string;
  /** 두께 — 실제 확인 후 채우는 값. 미입력이면 '문의'로 표시(임의 작성 금지). */
  thickness?: string;
  /** 크기 — 실제 확인 후 채우는 값. 미입력이면 '문의'로 표시. */
  size?: string;
  /** Set this to a path under /public (e.g. "/portfolio/led-neon-1.jpg") once a real photo is ready. */
  imageUrl?: string;
  /** 상세페이지 본문. 없으면 목록 카드만 노출되고 상세 링크가 생기지 않는다. */
  caseStudy?: CaseStudy;
}

